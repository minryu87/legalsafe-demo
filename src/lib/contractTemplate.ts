import type { ContractFormData } from "@/data/contract-types";

function formatDate(dateStr: string): string {
  if (!dateStr) return "____년 __월 __일";
  const d = new Date(dateStr);
  if (isNaN(d.getTime())) return "____년 __월 __일";
  return `${d.getFullYear()}년 ${d.getMonth() + 1}월 ${d.getDate()}일`;
}

function formatCurrency(amount: number): string {
  if (!amount) return "금 ____________원";
  return `금 ${amount.toLocaleString("ko-KR")}원`;
}

function blank(value: string, placeholder = "_______________"): string {
  return value || placeholder;
}

